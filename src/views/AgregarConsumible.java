        cons.descripcion=jTextDescripcion.getText();
        cons.tipo=jComboTipoConsumible.getSelectedItem();
        cons.precio=jTextPrecio.getText();
        cons.modoDeAdquisicion= jComboMododeAdquisicion.getSelectedItem();
        cons.tiempoDePreparacion=jTextTiempoPreparacion.getText();

        //Agregar consumible
        cons.AgregarConsumibles();
        Limpiar();
        this.dispose();

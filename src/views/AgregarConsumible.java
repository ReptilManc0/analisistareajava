    public void Validar(){
        
        //Validacion de Tiempo de Preparación
        jTextTiempoPreparacion.setEditable(false);
        if(jComboMododeAdquisicion.getSelectedItem().toString().equals("Preparación casera")){
            jTextTiempoPreparacion.setEditable(true);
            if(jTextTiempoPreparacion.getText().isEmpty()){
            jLabelTiempoPreparacion.setText("*");
            }else{
            jLabelTiempoPreparacion.setText("");
            }
        }else{
            jTextTiempoPreparacion.setEditable(false);
            jTextTiempoPreparacion.setText("");
            jLabelTiempoPreparacion.setText("");
        }
        
        // Validacion de Tipo
        if (jComboTipoConsumible.getSelectedItem().toString().equals("Seleccione")) {
            jLabelTipoConsumible.setText("*");
        } else {
            jLabelTipoConsumible.setText("");
        }
        
        // Validacion de Modo de Adquisicion
        if (jComboMododeAdquisicion.getSelectedItem().toString().equals("Seleccione")) {
            jLabelModoAdquisicion.setText("*");
        } else {
            jLabelModoAdquisicion.setText("");
        }
        
        //Validacion de nombre
        if(jTextNombre.getText().isEmpty()){
            jLabelNombre.setText("*");
        }else{
            jLabelNombre.setText("");
        }
        
        //Validacion de Descripcion
        if(jTextDescripcion.getText().isEmpty()){
            jLabelDescripcion.setText("*");
        }else{
            jLabelDescripcion.setText("");
        }

        //Validacion de Precio
        if(jTextPrecio.getText().isEmpty()){
            jLabelPrecio.setText("*");
        }else{
            jLabelPrecio.setText("");
        }
       //Validacion general
        if(jTextNombre.getText().isEmpty()||jTextDescripcion.getText().isEmpty()||jTextPrecio.getText().isEmpty()||jComboTipoConsumible.getSelectedItem().toString().equals("Seleccione")||jComboMododeAdquisicion.getSelectedItem().toString().equals("Seleccione")){
            Aceptar.setEnabled(false);
        }else{
            Aceptar.setEnabled(true);
        }
    }
